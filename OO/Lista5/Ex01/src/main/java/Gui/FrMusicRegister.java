    public void cleanField()
    {
        this.edtTitle.setText("");
        this.edtSingerName.setText("");
        this.edtTime.setText("");
        this.edtPrice.setText("");
    }
    
    public void enableField(boolean flag)
    {
        this.edtTitle.setEnabled(flag);
        this.edtSingerName.setEnabled(flag);
        this.edtTime.setEnabled(flag);
        this.edtPrice.setEnabled(flag);
    }
    
    public void objectForField(Music music)
    {
        this.edtTitle.setText(music.getTitle());
        this.edtSingerName.setText(music.getSingerName());
        this.edtTime.setText(music.getTime());
        this.edtPrice.setText(music.getPrice()+"");
    }
    public Music fieldForObject()
    {
        Music music = new Music();
        
        music.setTitle(this.edtTitle.getText());
        music.setSingerName(this.edtSingerName.getText());
        music.setTime(this.edtTime.getText());
        music.setPrice(Double.parseDouble(this.edtPrice.getText()));
        return music;
    }

