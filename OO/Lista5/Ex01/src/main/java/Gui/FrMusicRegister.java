    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        this.editing = true;
        this.oldTitle = JOptionPane.showInputDialog("Type the music title", "");
        
        Music editMusic = this.manager.findMusic(this.oldTitle);
        
        if(editMusic != null)
        {
            this.cleanField();
            this.enableField(true);
            
            objectForField(editMusic);
        }
        else
            JOptionPane.showMessageDialog(this, "Music not found!");
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        cleanField();
        enableField(false);
        this.editing = false;
    }//GEN-LAST:event_btnCancelActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        this.oldTitle = JOptionPane.showInputDialog("Type the music title", "");
        
        Music editPerson = this.manager.findMusic(this.oldTitle);
        
        if(editPerson != null)
        {
            this.manager.removeMusic(oldTitle);
            this.edtTextArea.setText(this.manager.toString());
            this.manager.saveToFile("list.xml");
        }
        else
            JOptionPane.showMessageDialog(this, "Music not found!");
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Music music = this.fieldForObject();
        if(this.editing == true)
            this.manager.updateMusic(this.oldTitle, music);
        this.manager.addMusic(music);
        this.cleanField();
        this.enableField(false);
        String print = this.manager.toString();
        
        this.editing = false;
        
        this.edtTextArea.setText(print);
        
        this.manager.saveToFile("list.xml");
    }//GEN-LAST:event_btnSaveActionPerformed

