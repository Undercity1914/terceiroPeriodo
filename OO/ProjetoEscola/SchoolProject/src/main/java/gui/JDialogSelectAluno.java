    private void grdAlunoMouseClicked(java.awt.event.MouseEvent evt) {                                       
       Student a = this.getObjetoSelecionadoNaGrid();
       this.objetoParaCampos(a);
    }
    
    public Student getObjetoSelecionadoNaGrid() {
        int linhaSelecionada = grdAluno.getSelectedRow();

        if (linhaSelecionada >= 0) {
            TMCadAluno tmCadAluno = (TMCadAluno) grdAluno.getModel();

             Student aluno = tmCadAluno.getObjetoAluno(linhaSelecionada);
            return aluno;
        }
        
        return null;
    }
    
    public void atualizarTabela() {
        TMCadAluno tmcadaluno;
        tmcadaluno = new TMCadAluno(gerenteA.getStudents());
        grdAluno.setModel(tmcadaluno);
    }
    

