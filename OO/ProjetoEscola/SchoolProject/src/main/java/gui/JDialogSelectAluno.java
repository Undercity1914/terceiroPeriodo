    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        Student a = this.alunoSelecionado;  // Obtém o aluno selecionado na interface
    
        if (a != null) {
            this.gerenteD.update(a.getName(), dis);
            gerenteD.salvarNoArquivo("ListagemDisciplinas.csv");
        
            JOptionPane.showMessageDialog(this, "Aluno matriculado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um aluno primeiro.");
        }
    
        this.limparCampos();
        this.habilitarCampos(false);
        this.atualizarTabela();
    }//GEN-LAST:event_btnSelecionarActionPerformed

