    private void initData(){
       this.gerenteA = new StudentManager();
        this.servico = new ServiceManager(gerenteA);
        this.gerenteD = new LessonManagement();
        this.Alunos =  new ArrayList<>();
        this.Disciplinas = new ArrayList<>();
        this.editando = true;

        servico.carregarDoArquivo("ListagemAlunos.csv");
        this.gerenteD.carregarDoArquivo("ListagemDisciplinas.csv");
        this.Disciplinas = gerenteD.getLessons();
        this.Alunos = gerenteA.getStudents();

        this.habilitarCampos(false);
        this.limparCampos();

        this.atualizarTabela();
    }
    
    public void habilitarCampos(boolean flag) {
        txtCPF.setEnabled(flag);
        txtMatricula.setEnabled(flag);
        txtNome.setEnabled(flag);
        txtIdade.setEnabled(flag);
        txtAnoIngresso.setEnabled(flag);
    }

    public void limparCampos() {
        txtCPF.setText("");
        txtMatricula.setText("");
        txtNome.setText("");
        txtIdade.setText("");
        txtAnoIngresso.setText("");
    }

    public void objetoParaCampos(Student p) {
       txtCPF.setText(p.getCpf()+"");
       txtMatricula.setText(p.getRegister()+"");
       txtNome.setText(p.getName());
       txtIdade.setText(p.getAge() + "");
       txtAnoIngresso.setText(p.getYearIn()+ "");

    }
    public Student camposParaObjeto() {
        Student p = new Student();
        
        p.setCpf(Integer.parseInt(txtCPF.getText()));

        p.setRegister(Integer.parseInt(txtMatricula.getText()));

        p.setName(txtNome.getText());

        p.setAge(Integer.parseInt(txtIdade.getText()));
        
        p.setYearIn(Integer.parseInt(txtAnoIngresso.getText()));

        return p;
    }
