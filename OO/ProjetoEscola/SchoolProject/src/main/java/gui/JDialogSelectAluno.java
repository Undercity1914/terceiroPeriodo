    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        Integer CPFEscolhido = Integer.parseInt(JOptionPane.showInputDialog("Informe o CPF do aluno que deseja selecionar:", ""));

        this.alunoSelecionado = (Student) this.servico.find(CPFEscolhido);

        if (alunoSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Não existe aluno com esse cpf.");
        } else {
            this.limparCampos();
            this.habilitarCampos(true);
            this.objetoParaCampos(alunoSelecionado);
            this.editando = true;
            
            this.objetoParaCampos(alunoSelecionado);
            this.editando = true;
            this.cpfEscolhido = alunoSelecionado.getCpf()+"";
        }

    }//GEN-LAST:event_btnPesquisarActionPerformed

